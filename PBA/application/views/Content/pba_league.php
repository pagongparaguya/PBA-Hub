          <h1>PBA League History</h1>
          <hr />  
          <p>The Philippine Basketball Association (PBA) is a men's professional basketball league in the Philippines composed of twelve company-branded franchised teams. It was the first professional basketball league in Asia and is the second oldest continuously existing in the world after the NBA. The league's regulations are a hybrid of rules from FIBA and the NBA.</p>
          <p>The league played its first game at the Araneta Coliseum in Quezon City on April 9, 1975. Its individual team offices are directed out of PBA head offices located in Eastwood City in Quezon City. The league is currently headed by Commissioner Chito Salud.</p>
